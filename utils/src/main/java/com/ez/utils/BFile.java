    public static void getALLDirImage(File file, List<String[]> imageItems) {
        _imageItems = imageItems;

        file.listFiles(new FileFilter() {
            @Override
            public boolean accept(File pathname) {
                String filename = pathname.getName().toLowerCase();
                if (filename.contains(".jpg")
                        || filename.contains(".png")
                        || filename.contains(".bmp")
                ) {
                    _imageItems.add(new String[]{filename.substring(0, filename.lastIndexOf(".")), pathname.getPath()});
                    return true;
                } else if (pathname.isDirectory()) {
                    getALLDirImage(pathname, _imageItems);
                }

                return false;
            }
        });
    }

