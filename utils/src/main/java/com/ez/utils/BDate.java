        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");
        //中文系统要加上这个 因为时间问题
        sdf.setTimeZone(TimeZone.getTimeZone("GMT"));
        String t1 = BDate.millis2String(currentDuration, sdf);
